#include <stdio.h>
#include <string.h>

int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int		main(void)
{
	char *i = "min";
	char *u = "minsu";
	printf("%d", ft_strcmp(i, u));
	printf("%d", strcmp(i, u));
}