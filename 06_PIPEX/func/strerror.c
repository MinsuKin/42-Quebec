#include "../pipex.h"

int main(void)
{
    char *err;

    printf("* * * * 에러 메시지 * * * *\n");
    for (int i = 0; i <= 108; i++)
    {
        err = strerror(i);
        printf("[%d]: %s\n", i, err);
    }
}