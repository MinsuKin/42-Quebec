char	*ft_strrchr(const char *s, int c)
{
	char	*ret;

	ret = (char *)s;
	while (*s != '\0')
		s++;
	while (s >= ret)
	{
		if (*s == (char)c)
			return ((char *)s);
		s--;
	}
	return (0);
}
